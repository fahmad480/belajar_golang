package main

import "fmt"

func main() {
	var name string

	name = "Faraaz Ahmad"
	fmt.Println(name)

	name = "Faraaz Permadi"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 30
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Faraaz"
		lastName = "Permadi"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
